
public class lien {
	private String _lien; 
	private String _ContenueLien;
	
	public lien(String _lien, String _ContenueLien) {
		super();
		this._lien = _lien;
		this._ContenueLien = _ContenueLien;
	}

	public String get_lien() {
		return _lien;
	}

	public void set_lien(String _lien) {
		this._lien = _lien;
	}

	public String get_ContenueLien() {
		return _ContenueLien;
	}

	public void set_ContenueLien(String _ContenueLien) {
		this._ContenueLien = _ContenueLien;
	} 
	
	
	

}
